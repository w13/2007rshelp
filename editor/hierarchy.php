<div class="title2">Maintenance Crew</div>
<p>The Maintenance Crew (MC) members are designated the task of maintaining our content and features, and also keeping them at a high standard. Content includes, but is not limited to: screenshots, fan concepts, guides, and maps. MC members are <b>expected to take initiative</b> in their work, as most of it is required just after the game updates - do not just limit yourself to the corrections listing.<br />
<b>Team Leaders:</b> Rashdan</p>

<u>Main Tasks involve:</u></p>
<ol>
<li>Fixing corrections submitted by our users.</li>
<li>Acquiring pictures needed for guides.</li>
<li>Helping with future Zybez projects, creatively and in producing them.</li>
</ol><p><u>Break down of tasks:</u></p>
<ol>
<li>Updating skill guides to include new items and pictures, ways to make money or train, changes in methods of making items, quests giving XP rewards to skills etc.</li>
<li>Updating city guides with relevant quest start points, icons, and images after RuneScape's weekly updates.</li>
<li>Updating quest guides with any changes to the plot or steps as the game changes.</li>
<li>Updating mini-game guides as new strategies or methods (incl. more powerful weapons/ armour) are written into the game.</li>
<li>Updating miscellaneous guides with new information as it is released.</li>
<li>Adding new mine and dungeon maps as they are added into the game.</li>
<li>Informing us of new items for the market price guide, item database or monster database.</li>
</ol>
<p><u>Quality Aspects:</u></p>

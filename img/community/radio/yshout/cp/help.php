<h3 class="first" id="password">Password</h3>
<p>The password you set here is the one you will use to log into the control panel. The default password is 'fortytwo', but you can change it to whatever you want. <a href="#header" class="totop">Back to top</a></p>


<h3 id="defaulttext">Default [nickname|message|submit] text</h3>
<p>These three preferences set the default text on the input elements and the submit button. On the input elements, the text appears grayed out and dissapears when they get focus. <a href="#header" class="totop">Back to top</a></p>

<h3 id="maxlength">Max. [message|nickname] length</h3>
<p>These two preferences control the maximum allowed length of the message and nickname, respectively. <a href="#header" class="totop">Back to top</a></p>


<h3 id="flood">Flood control</h3>
<p>The flood control in YShout 3 is revamped. While previous versions activated the flood control after each message, YS3 brings about a notable improvement. The flood control 
now allows X messages in Y seconds, and any more will trigger the flood control. In the example, X is the <em>flood timeout</em> preference, and Y is <em>flood messages</em>. The 
length of time that the flood control lasts is determined by the <em>flood length</em> preference. <a href="#header" class="totop">Back to top</a></p>


<h3 id="maxlogs">Maximum logs</h3>
<p>YShout allows you to have multiple shoutboxes, each with its own separate history. The amount of shoutboxes you can have is what this preference controls. I recommend leaving it 
as default unless you have an express purpose in changing it. <a href="#header" class="totop">Back to top</a></p>


<h3 id="historyshouts">Shouts to keep in history</h3>
<p>This one doesn't really need explaining, but for the sake of completeness I will anyway. This preference controls how many shouts YShout will keep in its log files. All older shouts 
will be deleted and never seen again. You may be tempted to set this preference to a really high number, but be warned that YShout may slow down if you set it too high. <a href="#header" class="totop">Back to top</a></p>


<h3 id="formpos">Form position</h3>
<p>This preference does exactly what it says: it controls whether the input form will be on top of the shouts or below. If you choose Below, then new messages will appear from the
 bottom and will make their way upwards. If you choose for the form to be on top, however, new messages will appear directly below the form and will scroll downwards. <a href="#header" class="totop">Back to top</a></p>


<h3 id="timestamp">Timestamp format</h3>
<p>This preference sets whether the timestamp shown next to messages is in 12- or 24-hour format. <a href="#header" class="totop">Back to top</a></p>


<h3 id="refresh">Refresh rate</h3>
<p>This preference controls how often YShout will poll the server for new shouts. The lower the number, the more instant conversations seem and the higher the CPU load. If you're 
looking to use it as a shoutbox more than a chat-style thingamabob, then you should heighten the refresh rate to 30 or 60 seconds. <a href="#header" class="totop">Back to top</a></p>


<h3 id="truncate">Truncate client-side</h3>
<p>This preference controls how many shouts are displayed at any given time. If new messages come in, older ones are scrolled off the top of the screen. <a href="#header" class="totop">Back to top</a></p>


<h3 id="cplink">Link to Admin CP</h3>
<p>This preference determines whether or not there will be a link to this control panel from YShout itself. You can always get to the control panel by typing in '/cp' (without quotes) as a message, and sending it. <a href="#header" class="totop">Back to top</a></p>


<h2>Processes</h2>
<p>This section thoroughly outlines the processes by which you, as a Content Team member, must go through in order to complete your designated tasks. </p>

<p><strong>New Content:</strong></p>
<blockquote>All currently-being-worked-on Content is to be be entered in the <a href="testing_area.php">Testing Area</a>. Please use our guide wizards when creating new guides to make sure they are in the right layout. Rename an entry to "DELETE" if, and only if, YOU are finished with it.<br /><br />
Leave notes in the test area notepad for what needs to be done to a guide. If a guide's content has been completed, have it checked over by someone else for spelling/grammar/structural/presentational issues.</blockquote>

<p><strong>Updating Content:</strong></p>
<blockquote>This is the primary task of the Maintenance Crew, and includes all of our guides (not databases). A full list of possible updates required can be found in Maintenance Crew's Job Description, <a href="hierarchy.php">here</a>. If there is a larger task, such as rewriting a section of a guide, or adding a new section, post what needs to be done in the <a href="index.php">main notepad</a>'s to do list. If you see something in there you can do, by all means, go ahead and do it. Simply tag your name onto the end of the item, and update the notepad.<br /><br />

If you have a badword filter installed on your computer that will remove words such as death, blood, sex, or cock from guides, TURN IT OFF IMMEDIATELY. After submitting a change to a guide, ALWAYS go to the website and LOOK at that guide to make sure nothing has messed up! If your internet connection drops out in the middle of submitting a guide, it will delete half the guide. The same goes for Internal Server Errors - if one occurs while you're updating, you will have cached an error page, and users won't be able to see the guide.
<br /><br />
<b>When making big changes to guides, always hit ctrl + a, then ctrl + c to copy the text to your clipboard. Remember that all of Zybez's pages are Cached. If when you update a guide you get a "Sorry, Cache update failed" message, go back and submit the guide again to update its cache.</b></blockquote>

<p><strong>Maintaining Content:</strong></p>
<blockquote>You will notice some sections have "instructions" that you are able to show and hide, provided you have javascript enabled. Please read, understand and follow those instructions carefully. ESPECIALLY in the databases.<br /><br />
We receive at least a hundred corrections per week, oftentimes many more; not including price reports (of which we receive several hundred reports/week). This means that every single day, there is something you can do. So no matter if you come on 3 days a week, or 7 days a week, there will still be something to do. When fixing a correction, <strong>you must ensure that what the member has submitted is correct.</strong> If it is incorrect, or not-needed, delete the report. If the member asks a question and submits their email address (hover over their name to see it), you may only send them a reply if you have an @zybez.net email account.
<br /><br />If a guide needs a new image, use the <a href="upload.php">Upload Images</a> form and detail in the Upload Images notepad, what it is to be used for.<br /><br />
With suspending and banning people from using Correction Submission, try to use your own judgement when either suspending them yourselves, or requesting them to be banned. In both cases, leave the correction there if you are unsure, and ask in the message box.<br />
<b>Request a ban if:</b> the text is random and stretches the page a lot; the user posted a keylogger or malware infected website; there is swearing or flaming of any kind; or if the user has spammed a number of corrections.<br />
<b>Suspend if:</b> the correction submission is not related to anything at all (Eg: I like apples and oranges); or only mild flaming and the user has a -2 or worse rating.</blockquote>

<p><strong>Maintaining Databases:</strong></p>
<blockquote>This includes the calculators, shop database, item database, market price database, and monster database. When updating entries in the database, ensure that you use correct data, spelling, and grammar. The most important thing is sticking to the instructions and filling in as much information as possible.</blockquote>

<p><strong>Notepads:</strong></p>
<blockquote>Anyone is allowed to edit the notepads to add information, or anything that needs to be updated. If you're making a large message in a notepad, or have not refreshed the page in a while, please be careful. Check to see if anyone has edited that notepad recently in the logbook. If they have, copy what you were going to post, refresh the page, add what you wanted to say and submit it. This is just so we don't have people overwriting other people's messages.<br /><br />

To get to the General Notepad, click the "zybez editor" text in the header image. Here you may:
<ol>
<li>Make comments <b>below</b> the To Do List.</li>
<li>Add tasks to the To Do List for others to do. <b>Delete them from the list when you've made the changes</b>.</li>
</ol></blockquote>


<h2>Introduction</h2>
<p><b>What:</b> The Zybez RuneScape Help Content Team consists of a large group of RuneScape experts who enjoy sharing their knowledge with other players.</p>

<p><b>What we do:</b> Develop new content; update current content; maintain content through correction submissions; update graphics for content; maintain the Zybez databases, and most importantly, we INNOVATE.</p>

<a name="2"></a>
<h2>Mission Statement</h2>
<p>This briefly defines the goals of the Content Team:</p>

<p align="center"><strong>Quality</strong> | <strong>Detail</strong> | <strong>Enthusiasm</strong> | <strong>Innovation</strong></p>

<p><strong>Quality:</strong> Maintaining quality and our professional appearance is our primary goal. The quality of our guides is achieved through ensuring that all members of the team are proficient in their literacy skills, and are properly instructed on how to develop them. Becoming part of the Content Team is a rigorous process, seeing as all of the team members need to be linguistically competant. Graphics must also be kept up to a spectacular standard through artistic talent, templates for similarity and more advanced graphic programs.</p>

<p><strong>Detail:</strong> To maintain the high standard of guides that is expected of Zybez RuneScape Help, every detail is taken into consideration prior to any release. All guides undergo a revision process, whereby team members ensure that content, spelling and grammar and visual aids are adequate and displayed well.</p>

<p><strong>Enthusiasm:</strong> Zybez RuneScape Help prides itself on the willingness of our massive user-base and Content Team to help. Enthusiasm is accomplished through ensuring that every member of the team is attaining useful experiences and enjoyment from their position. Being a member of the content team can teach you skills in xHTML, CSS and team work. If you become more involved further down the track, you may gain skills in Team Leadership/ Management and PHP.</p>

<p><strong>Innovation:</strong> Every successful organisation needs to innovate to stay ahead of the competition. Users don't want to see the same old content every single time they return to a website. They want to see something new, something fresh and interesting to use. Zybez is the leader in innovation for Runescape fans.</p>
